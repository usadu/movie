package com.etc.entity;

import org.springframework.stereotype.Component;

@Component
public class Danmu {
	private int danmuId;
	private int movieId;
	private String word;
	private String color;
	private int speed;
	private int top;
	private int fontSize;
	private String action;
	private int time;
	private String movieName;
	
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public int getDanmuId() {
		return danmuId;
	}
	public void setDanmuId(int danmuId) {
		this.danmuId = danmuId;
	}
	public int getMovieId() {
		return movieId;
	}
	public void setMovieId(int movieId) {
		this.movieId = movieId;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getFontSize() {
		return fontSize;
	}
	public void setFontSize(int fontSize) {
		this.fontSize = fontSize;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public Danmu() {
		// TODO Auto-generated constructor stub
	}
	
	public Danmu(int movieId, String word, String color, int speed, int top, int fontSize, String action, int time) {
		super();
		this.movieId = movieId;
		this.word = word;
		this.color = color;
		this.speed = speed;
		this.top = top;
		this.fontSize = fontSize;
		this.action = action;
		this.time = time;
	}
	@Override
	public String toString() {
		return "Danmu [danmuId=" + danmuId + ", movieId=" + movieId + ", word=" + word + ", color=" + color + ", speed="
				+ speed + ", top=" + top + ", fontSize=" + fontSize + ", action=" + action + ", time=" + time + "]";
	}
	
}
